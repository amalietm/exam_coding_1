# Write a program that outputs even if a number is even and odd if a number is odd. Think about what if the value is neither even nor odd (the value can be fractional or it can even not be a number at all).


num = int(input("Enter a number"))

if (num % 2) == 0:
	print("number is even")
elif (num % 2) == 1:
	print("number is odd")









